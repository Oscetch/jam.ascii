  createPlanetAscii(size, fillChar) {
    let ascii = "";
    let width = 1;
    const half = size / 2;
    for (let y = 0; y < size; y++) {
      const spaces = Math.ceil(half - width / 2);
      for (let x = 0; x < spaces; x++) {
        ascii += " ";
      }
      for (let x = 0; x < width; x++) {
        ascii += fillChar;
      }
      width += 2;
      if (y + 1 > half) {
        width -= 4;
      }
      ascii += "\n";
    }
    return ascii;
  },

